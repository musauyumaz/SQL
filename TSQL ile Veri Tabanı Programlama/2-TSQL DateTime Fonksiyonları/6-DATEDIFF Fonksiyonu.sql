-- 6-DATEDIFF Fonksiyonu
-- Ýki tarih arasýnda farký almak istediðimizde kullandýðýmýz fonksiyon.
SELECT 
DATEDIFF(YEAR,'02-14-1999',GETDATE()),
DATEDIFF(MONTH,'02-14-1999',GETDATE()),
DATEDIFF(WEEK,'02-14-1999',GETDATE()),
DATEDIFF(DAY,'02-14-1999',GETDATE()),
DATEDIFF(HOUR,'02-14-1999',GETDATE()),
DATEDIFF(MINUTE,'02-14-1999',GETDATE()),
DATEDIFF(SECOND,'02-14-1999',GETDATE())